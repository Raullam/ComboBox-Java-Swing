    public void metodeCarregaExercicis() {
        DataAccess da = new DataAccess();

        // Obtenim la lista de noms dels exercicis
        nombresEjercicios = da.getEjercicios();

        // Netejem el JComboBox abans d'agregar nous elements
        jComboBox1.removeAllItems();

        for (Exercicis e : nombresEjercicios) {
            jComboBox1.addItem(e.getNomExercici());
        }
        // Quan seleccionem un element del JComboBox, mostrar l'ID
    }

