    private void jComboBox1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jComboBox1ActionPerformed
        // Obtenim el nom seleccionat 
        String nomSeleccionat = (String) jComboBox1.getSelectedItem();
        if (nomSeleccionat != null) {
            // Buscar l'ID corresponent a partir del nom seleccionat
            for (Exercicis e : nombresEjercicios) {
                if (e.getNomExercici().equals(nomSeleccionat)) {
                    id = e.getId(); // Assignar l'ID a la variable global
                    System.out.println("ID de l'exercici seleccionat: " + id);

                    // Obtenir intents associats a l'exercici, amb el seu id busquem els intents d'aquell exercici
                    DataAccess da = new DataAccess();
                    ArrayList<Intents> intents = da.getIntentsByExerciciId(id);

                    // Crear un nou model de taula
                    DefaultTableModel dtm = new DefaultTableModel();

                    // Definim les columnes del model de taula amb el nom de la taula intents
                    dtm.addColumn("ID");
                    dtm.addColumn("ID Usuari");
                    dtm.addColumn("ID Exercici");
                    dtm.addColumn("Timestamp Inici");
                    dtm.addColumn("Timestamp Fi");
                    dtm.addColumn("Videofile");

                    // Afegim les files d'intents al model de taula
                    for (Intents intent : intents) {
                        dtm.addRow(new Object[]{
                            intent.getId(),
                            intent.getIdUsuari(),
                            intent.getIdExercici(),
                            intent.getTimestamp_Inici(),
                            intent.getTimestamp_Fi(), 
                            intent.getVideofile()
                        });
                    }

                    // Actualitzar el JTable amb el nou model
                    jTable1.setModel(dtm); // jTable1 Ã©s el teu JTable on vols mostrar intents
                    break;
                }
            }
        }
    }//GEN-LAST:event_jComboBox1ActionPerformed

